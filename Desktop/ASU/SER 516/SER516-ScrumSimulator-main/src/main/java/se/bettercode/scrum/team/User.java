package se.bettercode.scrum.team;

public class User {
    String name;
    int ID;
    String role;

    public String getName() {
        return name;
    }

    public void setName(String name){
        this.name = name;
    }

    public String getRole() {
        return role;
    }

    public void setRole(String role){
        this.role = role;
    }

    public int getID() {
        return ID;
    }

    public void setID(int ID) {
        this.ID = ID;
    }
}

